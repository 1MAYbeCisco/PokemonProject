CREATE DATABASE regionstable;
USE regionstable;

CREATE TABLE REGION
(
Region_ID INT NOT NULL,
Region_Name VARCHAR(10) NOT NULL,
PRIMARY KEY (Region_ID)
);

CREATE TABLE CITIES_TOWNS
(
    CT_ID INT NOT NULL,
    CT_Name VARCHAR(30) NOT NULL,
    CT_Population INT,
    Reg_ID INT NOT NULL,
    PRIMARY KEY (CT_ID, Reg_ID),
    FOREIGN KEY (Reg_ID) REFERENCES REGION(Region_ID)
);

CREATE TABLE ROUTES
(
    Route_ID INT NOT NULL,
    Route_Name VARCHAR(30) NOT NULL,
    Region_Num INT NOT NULL,
    Route_Environment VARCHAR(10) NOT NULL,
    PRIMARY KEY (Route_ID, Region_Num),
    FOREIGN KEY (Region_Num) REFERENCES REGION(Region_ID)
);


