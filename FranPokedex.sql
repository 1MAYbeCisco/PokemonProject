CREATE DATABASE FranPokedex;
USE FranPokedex;

CREATE TABLE TYPINGS
(
Typing_ID INT NOT NULL,
Typing_Name VARCHAR(10) NOT NULL,
PRIMARY KEY (Typing_ID)
);

CREATE TABLE POKEMON
(
Pokemon_Number INT NOT NULL,
Pokemon_Name VARCHAR(15) NOT NULL,
Pokemon_Typing1 INT NOT NULL,
Pokemon_Typing2 INT,
P_Height INT NOT NULL,
P_Weight INT NOT NULL,
Pokemon_Category VARCHAR(20) NOT NULL,
Pokedex_Desc TINYTEXT NOT NULL,
PRIMARY KEY (Pokemon_Number),
FOREIGN KEY (Pokemon_Typing1) REFERENCES TYPINGS(Typing_ID),
FOREIGN KEY (Pokemon_Typing2) REFERENCES TYPINGS(Typing_ID)
);

CREATE TABLE TRAINER
(
Trainer_ID INT NOT NULL AUTO_INCREMENT,
Trainer_Name VARCHAR(12) NOT NULL,
Amt_Money INT NOT NULL,
Pokedex_Ent INT NOT NULL,
Num_Badges INT NOT NULL,
Champion CHAR NOT NULL,
PRIMARY KEY (Trainer_ID)
);

CREATE TABLE POKEMON_TEAM
(
Train_ID INT NOT NULL AUTO_INCREMENT,
Pok_1 INT NOT NULL,
Pok_1_lvl INT,
Pok_2 INT NOT NULL,
Pok_2_lvl INT,
Pok_3 INT NOT NULL,
Pok_3_lvl INT,
Pok_4 INT NOT NULL,
Pok_4_lvl INT,
Pok_5 INT NOT NULL,
Pok_5_lvl INT,
Pok_6 INT,
Pok_6_lvl INT,
PRIMARY KEY (Train_ID, Pok_1, Pok_2, Pok_3, Pok_4, Pok_5),
FOREIGN KEY (Train_ID) REFERENCES TRAINER(Trainer_ID),
FOREIGN KEY (Pok_1) REFERENCES POKEMON(Pokemon_Number),
FOREIGN KEY (Pok_2) REFERENCES POKEMON(Pokemon_Number),
FOREIGN KEY (Pok_3) REFERENCES POKEMON(Pokemon_Number),
FOREIGN KEY (Pok_4) REFERENCES POKEMON(Pokemon_Number),
FOREIGN KEY (Pok_5) REFERENCES POKEMON(Pokemon_Number),
FOREIGN KEY (Pok_6) REFERENCES POKEMON(Pokemon_Number)
);

CREATE TABLE BASE_STATS
(
Base_Poke INT NOT NULL,
Base_HP INT NOT NULL,
Base_Atk INT NOT NULL,
Base_Def INT NOT NULL,
Base_Sp_Atk INT NOT NULL,
Base_Sp_Def INT NOT NULL,
Base_Speed INT NOT NULL,
PRIMARY KEY (Base_Poke),
FOREIGN KEY (Base_Poke) REFERENCES POKEMON(Pokemon_Number)
);

CREATE TABLE MOVES
(
Move_ID INT NOT NULL,
Move_Name VARCHAR(20) NOT NULL,
Move_Typing INT NOT NULL,
Move_Category VARCHAR(8) NOT NULL,
Power_Points INT NOT NULL,
Move_Power INT, 
Accuracy INT,
PRIMARY KEY (Move_ID),
FOREIGN KEY (Move_Typing) REFERENCES TYPINGS(Typing_ID)
);

CREATE TABLE NATURES
(
Nature_ID INT NOT NULL,
Nature_Name VARCHAR(10) NOT NULL,
Flavor_Like VARCHAR(10) NOT NULL,
Flavor_Dislike VARCHAR(10) NOT NULL,
PRIMARY KEY (Nature_ID)
);

CREATE TABLE ABILITIES
(
Ability_ID INT NOT NULL,
Ability_Name VARCHAR(20) NOT NULL,
Ability_Desc TINYTEXT NOT NULL,
PRIMARY KEY (Ability_ID)
);

CREATE TABLE RECEIVED_ABL
(
Num_Poke INT NOT NULL,
Abil_1 INT NOT NULL,
Abil_2 INT, 
Hidden_Abil INT,
PRIMARY KEY (Num_Poke, Abil_1),
FOREIGN KEY (Num_Poke) REFERENCES POKEMON(Pokemon_Number),
FOREIGN KEY (Abil_1) REFERENCES ABILITIES(Ability_ID),
FOREIGN KEY (Abil_2) REFERENCES ABILITIES(Ability_ID),
FOREIGN KEY (Hidden_Abil) REFERENCES ABILITIES(Ability_ID)
);


CREATE TABLE REGION
(
Region_ID INT NOT NULL,
Region_Name VARCHAR(10) NOT NULL,
PRIMARY KEY (Region_ID)
);

CREATE TABLE CITIES_TOWNS
(
    CT_ID INT NOT NULL,
    CT_Name VARCHAR(30) NOT NULL,
    CT_Population INT,
    Reg_ID INT NOT NULL,
    PRIMARY KEY (CT_ID, Reg_ID),
    FOREIGN KEY (Reg_ID) REFERENCES REGION(Region_ID)
);

CREATE TABLE ROUTES
(
    Route_ID INT NOT NULL,
    Route_Name VARCHAR(30) NOT NULL,
    Region_Num INT NOT NULL,
    Route_Environment VARCHAR(10) NOT NULL,
    PRIMARY KEY (Route_ID, Region_Num),
    FOREIGN KEY (Region_Num) REFERENCES REGION(Region_ID)
);

